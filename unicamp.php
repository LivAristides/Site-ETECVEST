<h6 class="fw-bold display-6 text-danger">Redação<h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">A redação da Unicamp vale 12 pontos na prova e assim tem um grande peso na prova. A Unicamp não especifica os gêneros textuais que serão cobrados e a cada ano os gêneros mudam, mas deixa em aberto dois temas de redação para o candidato escolher o tema ou gênero textual que tenham mais conhecimento ou seja mais confortável para escrever sobre.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span>As redações devem conter no máximo 22 linhas, portanto o texto deve ser objetivo e conciso. </p>
<h2 class="fw-bold display-7 text-dark">Requisitos:</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>Proposta temática: escrever sobre o tema proposto; </p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>Gênero: seguir o gênero textual solicitado; </p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>Leitura: ler os textos de apoio e utilizar as informações contidas neles; </p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>Articulação: a redação deve conter clareza, fluidez e demonstrar domínio das regras gramaticais. </p>  
<h2 class="fw-bold display-7 text-dark">Ultimos temas:</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">2022:</span></p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span> “Exposição das crianças na internet”</p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span>A UNICAMP inovou em suas propostas e na primeira proposta e foi solicitado que os candidatos escrevessem um “textão” como se fosse um post para as redes sociais.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span> “Cortes na verba para a ciência”</p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span>Na segunda proposta os candidatos deveriam se posicionar como um estudante na reunião da Sociedade Brasileira para o Progresso da Ciência (SBPC) e escrever um manifesto sobre os cortes de verbas para a pesquisa científica citada na reunião.</p>
