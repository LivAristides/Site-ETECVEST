<h6 class="fw-bold display-6 text-danger">Cursos Gratuitos<h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">Os cursos gratuitos e on-line são excelentes opções para adquirir conhecimentos e se manter competitivo no mercado de trabalho. Diversas instituições de ensino oferecem cursos gratuitos de curta duração para quem está interessado em se atualizar ou aprender um novo assunto.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span>Uma dessas oportunidades está sendo oferecida pela Escola de Extensão (Extecamp), da Universidade Estadual de Campinas (Unicamp), em parceria com a plataforma Coursera. No total, estão disponíveis 216 cursos gratuitos para estudantes de todo o Brasil.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span> Para ter acesso aos cursos o interessado precisa criar uma conta no site da Extecamp. Os cursos disponibilizados têm conteúdo criados por docentes da universidade e abrangem diferentes áreas, como tecnologia, administração e música, saúde, beleza, entre outras.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span> Ao concluir a carga horária mínima do curso o participante tem direito a certificado de conclusão, mas é necessário pagar uma taxa, devido à verificação de identidade. Contudo, para o estudante que não tiver condições financeiras de arcar com o custo do certificado é possível requerer auxílio para a emissão.</p>
