<h6 class="fw-bold display-6 text-danger">O que é a Unicamp?</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">A Universidade Estadual de Campinas é uma instituição pública e gratuita, 
fundada em 1966, mantida pelo governo do Estado de São Paulo. Seu principal campus está localizado em Campinas, 
a 90 km de São Paulo. Além de Campinas, as instalações da Unicamp se estendem ainda aos municípios de Piracicaba, 
onde fica a Faculdade de Odontologia (FOP), e Limeira, onde estão a Faculdade de Ciências Aplicadas (FCA) e a Faculdade de Tecnologia (FT),
possui também dois Colégios Técnicos, em Limeira (Cotil), em Campinas (Cotuca). A Universidade conta com 24 unidades de ensino, 
34 mil alunos matriculados em 66 cursos de graduação, 8 mil funcionários e 1800 docentes.</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"></p>
