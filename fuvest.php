<h6 class="fw-bold display-6 text-danger">Vestibular Fuvest 2024</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>	A primeira fase da Fuvest 2024 será realizada no dia 19 de novembro em 2023 e as provas da segunda fase estão marcadas para os dias 17 e 18 de dezembro de 2023. Já as provas de habilidades específicas acontecem entre os dias 03 e 06 de janeiro.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>	O processo seletivo da Fuvest funciona a partir de duas fases, ambas com caráter eliminatório, e ofereceu, em 2023, 11.147 vagas nas diversas graduações disponíveis. O período de inscrições para a Fuvest 2024 é entre o dia 17/08 a 06/10/2023.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>A taxa de inscrição do vestibular Fuvest 2024 segue sendo de R$ 191,00, mesmo valor de 2023. Em 2022 foi de R$ 182,00, ou seja, um aumento de R$ 9,00. O processo de solicitação de isenção acontece entre os dias 15 de maio a 14 de julho, no site da instituição que organiza o vestibular.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>	Quem solicitou a isenção da taxa, precisaria se encaixar em uma de três modalidades:</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Isenção da taxa de inscrição por renda bruta mensal individual ou familiar por indivíduo de, no máximo, R$ 1.745,32;	</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Redução de 50% da taxa de inscrição por renda bruta mensal individual ou familiar por indivíduo entre R$ 1.745,33 e R$ 3.490,65; e redução de 50% da taxa de inscrição por renda bruta mensal individual ou familiar por indivíduo de no máximo R$ 2.327,10, seguindo a Lei Estadual nº 12.782/2007.	</p>
<h2 class="fw-bold display-7 text-dark">Primeira fase</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>A primeira fase, que aconteceu no dia 04 de dezembro, conta com uma prova objetiva com 90 questões de múltipla escolha. Cada questão tem o peso de 1,0 ponto e os candidatos tem cinco horas para resolver as questões. As matérias cobradas nesta prova são:	</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	Biologia;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	Física;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	Geografia;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	História;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	Inglês;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	Matemática;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	Português;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	Química; e</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	Questões interdisciplinares.</p>
<h2 class="fw-bold display-7 text-dark">Segunda fase</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Já a segunda fase é dividida em dois dias de aplicação, que aconteceu nos dias 08 e 09 de janeiro, onde são feitas duas provas de natureza discursiva e de Conhecimentos Específicos em quatro horas de prova em ambos os dias.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>No primeiro dia é feita a prova de redação dissertativa-argumentativa e mais 10 questões dissertativas de Língua Portuguesa, que envolve compreensão e interpretação de textos, gramática e literatura.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Já a segunda prova varia de acordo com qual carreira o candidato escolher no período de inscrição. Ao todo somam 12 questões, acompanhe as variantes da segunda prova:</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	2 disciplinas: seis questões de cada;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	3 disciplinas: quatro questões de cada; e</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span>	4 disciplinas: três questões de cada.</p>
