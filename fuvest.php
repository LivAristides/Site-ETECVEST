<h6 class="fw-bold display-6 text-danger">História</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">O primeiro vestibular da Fuvest ocorreu em 19 de janeiro de 1977. Uma grande tempestade na noite da véspera do exame deixou São Paulo com vários pontos da cidade de inundação e bairros alagados no dia seguinte, sendo que a prova estava marcada para as 8 horas e tinha uma tolerância de 15 minutos de atraso. Devido a situação extraordinária, a coordenação da Fuvest permitiu a entrada de candidatos retardatários até as 11:15, mas esse prazo foi estendido até o início da tarde para alunos resgatados por helicópteros e enviados para o aeroporto Campo de Marte, onde dois hangares foram excepcionalmente utilizados para a realização do exame.
</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"> Ao longo de sua história, o vestibular da Fuvest passou por diversas mudanças. Na primeira metade da década de 1990, foi abolida a exigência de uma nota mínima que algumas carreiras exigiam, pois isso fazia com que sobrassem vagas – como no exame de 1988, quando 15% das vagas não foram preenchidas, e também na prova de 1990, que obrigou a fundação a realizar um segundo vestibular para preencher as vagas remanescentes.
</p>
    <p class="landing-content-desc mt-3" style="text-align: justify">Para o vestibular de 1995, a Fuvest aumentou de 72 para 160 questões da primeira fase e esses pontos também passaram a contar na nota final. Nessa época, a primeira fase passou a ser realizada em dois dias distintos.</p>
    <p class="landing-content-desc mt-3" style="text-align: justify">Em 2003, o número de questões da primeira fase foi reduzido para 100 e essa fase voltou a ser realiza em um único dia. Quatro anos depois, houve uma nova queda, desta vez para um total de 90 questões, mas com a manutenção do tempo máximo de cinco horas.</p>
    <p class="landing-content-desc mt-3" style="text-align: justify">A partir do exame de 2010, a Fuvest passou a aceitar pela primeira vez inscrição via internet. Outra mudança foi que primeira fase tornou-se apenas classificatória para a segunda fase, ou seja, sua nota não seria contabilizada para a nota final, e a segunda fase exigiria todas as disciplinas básicas do ensino médio, independente da área pretendida pelo vestibulando.</p>
    <p class="landing-content-desc mt-3" style="text-align: justify">Para o vestibular 2019, a Fuvest introduziu novidades como um sistema de reconhecimento facial dos candidatos para evitar fraudes e, pela primeira vez, um sistema de cotas, no qual 40% das vagas seriam destinadas a alunos oriundos de escola pública mais alunos de escola pública pretos, pardos e indígenas (PPI). Para o exame do vestibular seguinte, foram apresentadas provas impressas com figuras, mapas, gráficos coloridos.</p>
