<h6 class="fw-bold display-6 text-danger">O que é a Fuvest?</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">
De forma geral, a Fuvest é uma fundação brasileira de direito privada que se relaciona ao processo de admissão da Universidade de São Paulo (USP).
Isso porque, desde sua criação em 1976, objetivo central da fundação é organizar e realizar os exames vestibulares para admissão na USP.
</p>
    <p class="landing-content-desc mt-3" style="text-align: justify">Em outras palavras, a Fuvest é responsável por produzir o vestibular da USP que, inclusive, está entre os mais concorridos do país.
    Os dados indicam que o vestibular de 2023 da Fuvest recebeu 114.432 inscrições; o vestibular 2022 recebeu 110.383 inscrições e 130.525 em 2021.
</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"></p>
    <p class="landing-content-desc mt-3" style="text-align: justify">Sendo assim, o vestibular da Fuvest acontece em duas fases e é a segunda maior prova do país, depois do Enem.</p>
</br>
</br>

