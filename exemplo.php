<h2 class="fw-bold display-7 text-dark">Enem 2020</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">Tema:</span> O estigma associado às doenças mentais na sociedade brasileira</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">Autor:</span> Julia Vieira </p>
</br>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>
No filme estadunidense “Coringa”, o personagem principal, Arthur Fleck, sofre de um transtorno mental que o faz ter episódios de riso exagerado e descontrolado em público, motivo pelo qual é frequentemente atacado nas ruas. Em consonância com a realidade de Arthur, está a de muitos cidadãos, já que o estigma associado às doenças mentais na sociedade brasileira ainda configura um desafio a ser sanado. Isso ocorre, seja pela negligência governamental nesse âmbito, seja pela discriminação desta classe por parcela da população verde-amarela. Dessa maneira, é imperioso que essa chaga social seja resolvida, a fim de que o longa norte-americano não mais reflita o contexto atual da nação.
</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Nessa perspectiva, acerca da lógica referente aos transtornos da mente, é válido retomar o aspecto supracitado quanto à omissão estatal neste caso. Segundo a OMS (Organização Mundial da Saúde), o Brasil é o país que apresenta o maior número de casos de depressão da América Latina e, mesmo diante desse cenário alarmante, os tratamentos às doenças mentais, quando oferecidos, não são, na maioria das vezes, eficazes. Isso acontece pela falta de investimento público em centros especializados no cuidado para com essas condições. Consequentemente, muitos portadores, sobretudo aqueles de menor renda, não são devidamente tratados, contribuindo para sua progressiva marginalização perante o corpo social. Este quadro de inoperância das esferas de poder exemplifica a teoria das Instituições Zumbis, do sociólogo Zygmunt Bauman, que as descreve como presentes na sociedade, mas que não cumprem seu papel com eficácia. Desse modo, é imprescindível que, para a refutação da teoria do estudioso polonês, essa problemática seja revertida.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Paralelamente ao descaso das esferas governamentais nessa questão, é fundamental o debate acerca da aversão de parte dos civis ao grupo em pauta, uma vez que ambos são impasses para sua completa socialização. Esse preconceito se dá pelos errôneos ideais de felicidade disseminados na sociedade como metas universais. Entretanto, essas concepções segregam os indivíduos entre os “fortes” e os “fracos”, em que tais fracos, geralmente, integram a classe em discussão, dado que não atingem essas metas estabelecidas, como a estabilidade emocional. Por conseguinte, aqueles que não alcançam os objetivos são estigmatizados e excluídos do tecido social. Tal conjuntura segregacionista - os que possuem algum tipo de transtorno, nesse caso - na teia social. Dessa maneira, essa problemática urge ser solucionada para que o princípio da alemã seja validado no país tupiniquim. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Portanto, são essenciais medidas operantes para a reversão do estigma associado às doenças mentais na sociedade brasileira. Para isso, compete ao Ministério da Saúde investir na melhoria da qualidade dos tratamentos a essas doenças nos centros públicos especializados de cuidados, destinando mais medicamentos e contratando, por concursos, mais profissionais da área, como psiquiatras e enfermeiros. Isso deve ser feito por meio de recursos autorizados pelo Tribunal de Contas da União - órgão que opera feitos públicos - com o fito de potencializar o atendimento a esses pacientes e oferecê-los um tratamento eficaz. Ademais, palestras devem ser realizadas em espaços públicos sobre os malefícios das falsas concepções de prazer e da importância do acolhimento dos vulneráveis. Assim, os ideais inalcançáveis não mais serão instrumentos segregadores e, finalmente, a cotação de Fleck não mais representará a dos brasileiros.</p>
