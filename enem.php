    <div class="container">
      <div class="col-md-12 mx-auto text-right" id="correção">
<h6 class="fw-bold display-6 text-danger">Como é feita a correção da redação</h6>
</div>

<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<h2 class="fw-bold display-7 text-dark"></h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Cada competência vale de 0 a 200 pontos, totalizando a nota máxima de 1000 pontos:</p>

<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Competência 1: demonstrar domínio da modalidade escrita formal da língua portuguesa.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Competência 2: compreender a proposta da redação e aplicar conceitos das várias áreas de conhecimento para desenvolver o tema dentro dos limites estruturais. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Competência 3: selecionar, relacionar, organizar e interpretar informações, fatos, opiniões e argumentos em defesa de um ponto de vista. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Competência 4: demonstrar conhecimento dos mecanismos linguísticos necessários para a construção da argumentação. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Competência 5: elaborar proposta de intervenção para o problema abordado que respeite os direitos humanos. </p>
</br>
<h2 class="fw-bold display-7 text-dark">Como são feitas as correções</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>As correções são feitas numa plataforma online, na qual o avaliador recebe os textos sem identificação. Cada avaliador recebe até 200 avaliações por dia e deve avaliar mais de 150 textos a cada três dias. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Todas as redações são corrigidas por dois avaliadores que desconhecem a nota atribuída pelo outro. A nota final é a média aritmética das duas notas. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>A redação do Enem vale 1000 pontos. Essa nota é formada pela pontuação das 5 competências que foi mencionado anteriormente. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Os professores da banca examinadora dão uma nota entre 0 e 200 pontos para cada uma das competências. A nota final da redação é média aritmética das notas totais dadas pelos professores. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Em caso de divergência de 80 pontos em uma das competências ou maior que 100 pontos no total, a redação é avaliada por um terceiro corretor. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Se a redação continuar com notas muito diferentes, ela será avaliada por uma banca presencial de três professores. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>A nota final da redação é a média aritmética das duas notas que mais se aproximarem. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> </p>
