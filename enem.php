<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span> Reserve um tempinho para se exercitar, dormir bem e dedicar tempo às atividades de lazer, a escolha indicada é o domingo.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span> Antes do Exame, nada de virar noite ou se encher de bebidas com cafeína (café, energéticos…). Seu cérebro precisa estar bem descansado.</p>
