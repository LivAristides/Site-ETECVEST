<section id="enem" class="section-padding devider">
    <div class="container">
      <div class="col-md-12 mx-auto text-right" id="onde">
<h6 class="fw-bold display-6 text-danger">Onde usar a nota do Enem</h6>
</div>
</br>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<h2 class="fw-bold display-7 text-dark">Entrar em uma universidade pública pelo Sisu</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> O Sisu é um programa em que os candidatos são escolhidos pelas notas do Enem. </p>

<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>As inscrições são abertas duas vezes ao ano (em janeiro e junho), e para participar é obrigatório ter feito a edição mais recente do Enem e não ter zerado na redação.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Algumas universidades oferecem apenas uma porcentagem das vagas e o restante é preenchido através do vestibular próprio da instituição. Outras já aboliram completamente o vestibular tradicional e disponibilizam todas as suas vagas por meio do Sisu.</p>
</br>
</br>
<h2 class="fw-bold display-7 text-dark">Conseguir uma bolsa de estudos pelo ProUni</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> O Programa Universidade para Todos concede bolsas parciais e integrais a estudantes de baixa renda que participaram do Enem. O candidato precisa ter feito a última edição do Exame. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Para as vagas que não preenchidas, abre-se um novo período de inscrição. Neste caso, quem fez o Enem em anos anteriores (a partir de 2010) pode concorrer às bolsas remanescentes, as inscrições abrem em janeiro e junho.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> É exigido que os candidatos tenham atingido um mínimo de 450 pontos na média das provas e nota superior a zero na redação. Já para a avaliação socioeconômica, é preciso ter uma renda familiar de até 1,5 salário-mínimo por pessoa para bolsas de 100% e de até 3 salários-mínimos para bolsas de 100%. </p>
</br>
</br>
<h2 class="fw-bold display-7 text-dark">Complementar a nota do vestibular</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Algumas universidades, principalmente as públicas, costumam aceitar a nota do Enem como parte da pontuação do vestibular tradicional. O candidato precisa fazer as duas provas: do Enem e do vestibular.
</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Mas lembre-se, nem todas as universidades possuem esse método! </p>
