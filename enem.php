<h6 class="fw-bold display-6 text-danger">Como usar as notas do Enem para estudar em Portugal?</h6>
</div>

<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<h2 class="fw-bold display-7 text-dark"></h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Se o seu objetivo é utilizar as notas do ENEM para estudar em Portugal, saiba que estas são aceitas em Portugal para o processo de seriação dos estudantes brasileiros que têm interesse em concorrer a uma graduação neste país.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Vale informar que o curso de graduação em Portugal é chamado de 1º Ciclo – Licenciatura. Em Portugal não há bacharelado. Alguns poucos cursos também podem ser encontrados como Mestrado Integrado (Licenciatura + Mestrado).</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Importante: Se o estudante e/ou um dos pais tem uma nacionalidade europeia, não poderá utilizar as notas do ENEM. O processo é outro. Saiba mais sobre forma de ingresso do estudante internacional e do nacional aqui.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> </p>
</br>
<h2 class="fw-bold display-7 text-dark">Quem pode utilizar as Notas do Enem para estudar em Portugal?</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Apenas estudantes brasileiros, que não tenham cidadania europeia e/ou também não tenham pais com cidadania europeia, afinal, caso possua esta relação, o estudante perde o estatuto de estudante internacional e passa a ser considerado um estudante nacional europeu, devendo seguir as regras deste para o ingresso nas universidades. Se este for o seu caso, veja aqui como ingressar nas universidades sendo europeu.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> O ENEM não tem efeito para concorrer a curso de Mestrado, Pós-Graduação ou Doutorado, por ser exclusivo, a estudantes brasileiros com interesse em graduação, ou seja, licenciatura em Portugal.</p>
</br>
<h2 class="fw-bold display-7 text-dark">Há nota de corte do Enem para estudar em Portugal?</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Cada universidade tem a sua própria nota de corte do ENEM e está ainda pode variar por curso. De uma forma geral, com notas a partir de 475 pontos o estudante brasileiro pode concorrer a vagas em instituições públicas e privadas em Portugal. Geralmente, são válidas as notas do ENEM dos últimos 4 (quatro) anos.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Este concurso é conhecido como o de Estudante Internacional. Isso significa que 30% das vagas das licenciaturas e mestrados integrados em Portugal ficam reservadas aos Estudantes Internacionais, situação na qual o estudante brasileiro se enquadra.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Lembre-se que para utilizar o ENEM é preciso atender às regras do Estatuto do Estudante Internacional.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-daark">Nota: </span> Há algumas instituições portuguesas que aceitam notas mais antigas! E para quem pretende uma transferência da graduação do Brasil para Portugal, também algumas instituições aceitam a declaração com as notas do vestibular por matéria. O vestibular no Brasil apenas por redação não é aceito.</p>
</br>
<h2 class="fw-bold display-7 text-dark">Como saber se suas notas do ENEM para estudar em Portugal são suficientes?</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Cada instituição de ensino superior apresenta as regras de uso das notas do ENEM para estudar em Portugal nos seus regulamentos ou editais.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Nem todas as notas das provas do ENEM são utilizadas para o processo seletivo. Então, vamos entender isso na prática.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Se um estudante pretende ingressar para a Licenciatura em Direito da Universidade de Coimbra, por exemplo, serão considerados os seguintes pesos nas provas de:</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Redação: 45% mais Ciências Humanas e suas Tecnologias: 45% mais Nota Global do ENEM: 10%</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> O resultado deve ser igual ou superior a 600 pontos, para que o estudante esteja apto a participar do processo seletivo da Universidade de Coimbra.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Cada universidade tem suas regras de ponderação das notas do ENEM para o curso pretendido. Portanto, cabe uma análise criteriosa do curso pretendido nas diversas instituições portuguesas antes de decidir onde, de fato, pretende se candidatar. É neste sentido que a EduPortugal oferece a assessoria nas candidaturas aos interessados em estudar em Portugal.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Psiu! A taxa de aceitação dos estudantes internacionais que passam pela assessoria da EduPortugal é de 98%.</p>
</br>
<h2 class="fw-bold display-7 text-dark">Documentos Necessários para a candidatura em uma graduação em Portugal</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Além das notas oficiais do ENEM, o estudante deve apresentar o certificado e o histórico de conclusão do ensino médio com Apostilha de Haia. Em alguns casos, há universidades que exigem a equivalência do ensino médio brasileiro ao ensino secundário português. E este é um ponto muito importante, pois se o estudante concluiu o ensino médio via supletivo, EJA ou por exames, estes não são reconhecidos em Portugal para a obtenção de uma equivalência ao ensino secundário português. Isso significa que este (a) estudante ficará restrito (a) a concorrer apenas às universidades que não exigirem tal equivalência.</p>
</br>
<h2 class="fw-bold display-7 text-dark">Não Tenho Enem Válido Para Estudar Em Portugal. E Agora?</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Caso não você não tenha notas do ENEM ou sejam muito antigas, há uma solução para este caso. Muitas universidades abrem a possibilidade de realização de prova de ingresso (similar ao vestibular) ao estudante que não tem notas do ENEM. Estas provas, em alguns casos, podem ser feitas presencialmente ou de forma remota (online).</p>
