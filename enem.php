<h2 class="fw-bold display-7 text-dark">Entrar em uma universidade privada sem vestibular</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> Algumas instituições oferecem até mesmo descontos nas mensalidades ou bolsas de estudo para aqueles que tiveram um alto rendimento na prova. </p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span> A nota mínima exigida varia de um curso para outro e de uma universidade para outra. De modo geral, são aceitas as notas de qualquer edição do Enem a partir de 2010.</p>
