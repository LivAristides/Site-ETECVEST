<h2 class="fw-bold display-7 text-dark">2 – Memórias Póstumas de Brás Cubas, Machado de Assis</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Memórias Póstumas de Brás Cubas mistura filosofia, história e crítica social. A infância de Brás Cubas, como a de todo membro da sociedade patriarcal brasileira da época, é marcada por privilégios parentais. O livro tem um conteúdo histórico e crítica social escancarada.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span></p>
</br>

<h2 class="fw-bold display-7 text-dark">3 – Grande Sertão veredas, Guimarães Rosa</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Trata-se de uma obra legítima e original, em que prestigiado autor usa, em toda a obra, a própria linguagem sertaneja, dividindo opiniões, sendo ela uma das obras mais importantes da literatura brasileira.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span></p>
</br>

<h2 class="fw-bold display-7 text-dark">4 – Iracema, José de Alencar</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>Sendo parte da tríade indianista de José de Alencar, este romance canônico retrata as maravilhas naturais do Brasil e do seu povo, narrando uma história de amor entre um colonizador e uma nativa. Sendo caracterizada por ser da 1 fase do romantismo brasileiro e uma das mais importantes.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span></p>
</br>

<h2 class="fw-bold display-7 text-dark">5 – O Cortiço, Luiz Azevedo</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>O cortiço é uma grande obra do Naturalismo, narrada num povoado onde a trajetória dos personagens tem idas e vindas, a obra mostra para o leitor o comportamento do homem perante as situações, seu caráter também é analisado.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span></p>
</br>

<h2 class="fw-bold display-7 text-dark">6 – Capitães de areia, Jorge Amado</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>A história fala sobre um grupo de meninos de rua, assaltos e comportamentos típicos de uma vida banalizada pela sociedade brasileira. É notável a mescla de sentimentos a serem explorados, contando com violência, abandono, adultização e segregação familiar.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span></p>
</br>

<h2 class="fw-bold display-7 text-dark">7 – A hora da estrela, Clarice Lispector</h2>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>A obra marca a 3º geração Modernista da nossa Literatura, com uma narrativa instigante, original e autobiográfica, o conto traz sentimentos pessoas e angustia do personagem principal.</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger"></span>A trama marca profundidades psicológicas, revelações e mistérios que fazem parte de narrativas modernas.</p>
