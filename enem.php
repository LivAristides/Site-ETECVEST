<h6 class="fw-bold display-6 text-danger">O que é o Enem?</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">Enem é a sigla de Exame Nacional do Ensino Médio, uma avaliação educacional criada pelo Ministério da Educação que serve como forma de
     ingresso nas instituições de ensino superior. O Enem foi criado, inicialmente, para avaliar o desempenho de estudantes que terminavam 
     o ensino médio.</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"> Quando foi lançado pelo MEC, em 1998, o exame tinha como objetivo avaliar a qualidade do ensino brasileiro e refletir sobre como ele 
        poderia ser melhorado. A partir de 2004, o Enem passou a ser usado como forma de ingresso em instituições de ensino superior.
</p>
</br>
</br>


<section id="enem" class="section-padding devider">
    <div class="container">
      <div class="col-md-12 mx-auto text-right" id="quem">
<h6 class="fw-bold display-6 text-danger">Quem pode fazer o enem?</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">Podem fazer o Enem os estudantes que estão cursando o
 terceiro e último ano do ensino médio. Alunos do primeiro e do segundo ano também podem fazer a prova, mas na condição de treineiros,
  ou seja, somente para conhecer como funciona o exame. Também podem fazer o 
Enem candidatos que já concluíram o ensino médio, independentemente da idade e de quando terminaram essa etapa escolar.</p>
    
</br>
</br>


<section id="enem" class="section-padding devider">
    <div class="container">
      <div class="col-md-12 mx-auto text-right" id="pq">
<h6 class="fw-bold display-6 text-danger">Pra que serve?</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">O Enem é uma prova que avalia o conhecimento e 
as habilidades que os estudantes adquiriram ao longo do ensino médio. Para isso, eles são avaliados por meio de 180 questões 
objetivas de quatro áreas do conhecimento e por uma redação.
</p>
<p class="landing-content-desc mt-3" style="text-align: justify">As questões objetivas da prova do Enem são divididas 
em quatro áreas de conhecimento:
</p>
<p class="landing-content-desc mt-3" style="text-align: justify">  <span class="text-danger">-</span> Linguagens, Códigos e suas Tecnologias (Língua Portuguesa, Língua Estrangeira, Literatura e Artes);
</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span> Matemática e suas Tecnologias;</p>
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span> Ciências Humanas (História, Geografia, Sociologia e Filosofia);</p>  
<p class="landing-content-desc mt-3" style="text-align: justify"> <span class="text-danger">-</span> Ciências da Natureza (Biologia, Física e Química).</p>
<p class="landing-content-desc mt-3" style="text-align: justify">Sendo assim, por exemplo, ao invés de provas de disciplinas isoladas, 
como Biologia, o estudante é avaliado por meio de um conjunto de provas que engloba as Ciências da Natureza — Biologia, Física e Química 
— e que apresenta, geralmente, questões interdisciplinares.
</p>
<p class="landing-content-desc mt-3" style="text-align: justify">Outra característica específica do 
Enem é a redação. Os alunos devem elaborar um texto dissertativo-argumentativo. Normalmente, eles recebem 
um tema de cunho social e, então, precisam desenvolver uma proposta de intervenção, que é uma solução viável para o problema abordado.
</p>
<p class="landing-content-desc mt-3" style="text-align: justify"></p>

