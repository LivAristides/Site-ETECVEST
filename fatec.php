    <h4 class="fw-bold display-8 text-dark">Português</h4>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger"></span>Estudo dos gêneros e tipos textuais (literários e não-literários) e de sua materialidade linguística (pertinência e adequação dos empregos realizados) serão os critérios gerais para a avaliação das habilidades do candidato.</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	O apuro na organização gramatical da frase;</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	a adequação do emprego vocabular (bem como seus processos de formação morfológica e usos) e das classes de palavras (substantivos, adjetivos, advérbios, verbos, numerais, preposições, conjunções, pronomes, artigos e interjeições);</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	as relações sintáticas de dependência entre as palavras na oração (regência nominal);</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	a capacidade de grafar corretamente as palavras (ortografia, acentuação);</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	de empregar, com precisão, marcadores de número, de pessoa e de gênero (morfemas e desinências flexionais, flexão e concordância);</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	o emprego adequado dos verbos na oração, provendo a relação modal e temporal (correlação de tempos e modos) bem como suas relações com os termos a ele subordinados (regência verbal);</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	de desenvolver períodos com a necessária relação sintático-semântica entre frases e orações (coesão e coerência, conjunções, pronomes relativos, preposições e operadores argumentativos textuais);</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	de empregar adequadamente as vozes do verbo em função das construções e da natureza do texto;</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span> os recursos estilísticos, tais como as figuras de linguagem e recursos estruturais;</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span> a construção e organização sintática das frases e dos períodos (relação entre classes de palavras e funções sintáticas e semânticas) assim como os efeitos discursivos (intenções via codificação linguística e objetivos);</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	o conhecimento das literaturas brasileira, portuguesa e sua relação intertextual com as produções do mundo (incluindo-se produções dos países de língua portuguesa), nos vários períodos de produção (períodos literários e intertextualidade);</p>
    <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-danger">-</span>	a relação entre a produção literária e a realidade cultural e histórica em que se produziram os textos.</p>
